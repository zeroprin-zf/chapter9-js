$(function(){
  $('#back a').on('click', function(event){
    //#back内のaタグがクリックされたときの処理
    //('id名 要素名')と指定すると、id内の子要素に対してのみ指定される
    //id = "back"内のaタグがクリックされたときに処理が行われるような記述
    //クリック後に複数のイベントが行われるようにするために第1引数にイベント名click
    //第2引数に関数を指定
    $('body, html').animate({//animateはアニメーション効果を設定するjQueryの関数
      scrollTop:0//最上部に移動する
    }, 800);//0.8秒間で移動する
    event.preventDefault();//aタグの機能を無効にする
  })
})

